public class BaoCaoTongKetMonDTO {
    private String tenLop;
    private int siSo;
    private float soLuongDatMon;
    private float tiLeDatMon;

    /**
     * @return the tenLop
     */
    public String getTenLop() {
        return tenLop;
    }

    /**
     * @param tenLop the tenLop to set
     */
    public void setTenLop(String tenLop) {
        this.tenLop = tenLop;
    }

    /**
     * @return the siSo
     */
    public int getSiSo() {
        return siSo;
    }

    /**
     * @param siSo the siSo to set
     */
    public void setSiSo(int siSo) {
        this.siSo = siSo;
    }

    /**
     * @return the soLuongDatMon
     */
    public float getSoLuongDatMon() {
        return soLuongDatMon;
    }

    /**
     * @param soLuongDatMon the soLuongDatMon to set
     */
    public void setSoLuongDatMon(float soLuongDatMon) {
        this.soLuongDatMon = soLuongDatMon;
    }

    /**
     * @return the tiLeDatMon
     */
    public float getTiLeDatMon() {
        return tiLeDatMon;
    }

    /**
     * @param tiLeDatMon the tiLeDatMon to set
     */
    public void setTiLeDatMon(float tiLeDatMon) {
        this.tiLeDatMon = tiLeDatMon;
    }
}
